          <h2>Executive Team</h2>
          <div class="clr"></div>
          <p><strong id="JC">Jason Chen, President</strong><br /><br />
            <img class='profile' src="images/Jason_Chen.png" />
          
            Jason Chen is currently the President of Bear and Bull Media Group. Jason is in his final year of studies in Economics, with a specialization in Finance. He has a strong interest in the financial markets, particularly in bonds, stocks, foreign exchange, commodities, and derivatives. He has won several stock simulator competitions and enjoys discussions regarding the economy and financial markets. He has written several articles in financial services and he will also be writing weekly summaries consisting of weekly analysis of the stock markets and a preview of expected market activity for next week.<br /><br />

Jason is pursuing the CFA designation. He has successfully completed the Canadian Securities Course and will soon also complete the Conduct and Practices Handbook. His other interests include soccer, politics, and consumer electronics. <br /><br />

            <strong id="JL">Jaron Liu, Editor-in-Chief</strong><br /><br />
            <img class='profile' src="images/Jaron_Liu.png" />
            
            Jaron is entering his third-year of studies as an Economics major with a specialization in Finance at the University of Waterloo. As Editor-in-Chief, his goal is to help Bear & Bull succeed by encouraging and working with highly motivated individuals in producing informative and stimulating articles, in a timely and professional manner. A Toronto-native, he first became involved with Bear & Bull in his second-year where he thoroughly enjoyed the position as an Economic Analyst. He is very excited to take on the new role as Editor-in-chief and hopes to read some interesting and fascinating articles produced 
by the team. <br /><br />

During his spare time he enjoys basketball (please don't mention the Raptors), music, and visual arts.<br /><br />
            <strong id="AW">Alex Williams, Vice President Marketing</strong><br /><br />
            <img class="profile" src="images/Alex_Williams.png"  />
            Alex is a second year student in Honours Mathematics, completing a double major in Mathematical Finance and Economics at the University of Waterloo. His role, as the VP Marketing, is to promote Bear and Bull and increase student awareness, participation and market reach in both readers and analysts.<br /><br />

Some of the new projects Alex envisions to implement during this term include: guest speaker events, professional training package, and e-newsletters. Members of the Bear and Bull Marketing Team will have many great opportunities to express their creativity and professionalism through leading and participating in these projects.<br /><br />

Alex is pursuing the CFA designation. He is also an active member of the UW Finance Association. During his leisure time, Alex enjoys reading about the economy and the markets, he is also interested in options strategies, and algorithmic trading strategies. Alex also enjoys running and snowboarding in his spare time. <br /><br />
          <strong id="ZH">Zac Hogg, Vice President Administration and Finance</strong><br /><br />
          <img class='profile' src="images/Zac_Hogg.png"  />
          Zac Hogg is a second year student at the University of Waterloo in the Accounting and Financial
Management program. As VP of admin and finance at the Bear and Bull Media Group, Zac's objective is to ensure all the financial needs of the club are met in order to successfully run events
as well as everyday operations. In addition, Zac is also responsible for admin duties that help the
club operate efficiently. Zac has experience in both these capacities through his years of Junior
Achievement, in high school, where he was VP Finance in grade 11 and company President and VP
Finance in grade 12.<br /><br />

In addition, Zac is involved in extra-curricular activities on and off campus, including the School of
Accounting and Finance (SAF) News Team, which he manages, and participating in accounting
case competitions. Zac is also involved with many other initiatives inside the SAF as a Fellowship
Member, including volunteering at on campus events like You@Waterloo Day, and off campus
events like the Ontario Universities' Fair at the Metro Toronto Convention Centre. In his free time,
Zac enjoys playing sports, especially ice and ball hockey, drinking protein after workouts, eating with
chopsticks, and socializing with friends.<br /><br />
          
          <strong id="HW">Haylin Wang, Vice President Information Technology</strong><br /><br />
          <img class='profile' src="images/Haylin_Wang.png"  />
          Haylin is a first year Public Accounting student in the Accounting and Financial Management Program and a Fellow of the School of Accounting and Finance. Her role, as the VP IT, is to maintain and update Bear and Bull's website and database. She is also responsible for facilitating communication among executives, developers, and end users, regulating electronic article and report submission, and resolving technical difficulties and improving user experience.<br /><br />
 
In Winter 2011 Term, as VP Marketing, Haylin designed our new website (the one you are viewing) and database with two bright first year Electric and Computer Engineering students, Edward Zhang and Vishvadeep Vig. Instead of Flash, the new website was designed using mainly HTML, XML, CSS, and Javascript. It is now much more organized, faster, stable, functional, compatible and comfortable for both end users and developers. Haylin is devoted in continuously improving front-end user interface and database maintenance efficiency. Two user experience surveys will be conducted in June and July.<br /><br />
 
Haylin is pursuing a CA and a CFA designation. She is also an active member of the SAF Ambassador Program, Toastmasters, University of Waterloo Choir, Yoga Club, and various campus events teams. During her leisure time, she is an enthusiastic volunteer, singer, voice actor, and avid reader.<br /><br />
