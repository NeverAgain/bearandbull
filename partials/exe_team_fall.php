          <h2>Executive Team</h2>
          <div class="clr"></div>
          <p><strong id="KY">Kenny Yang: President</strong><br /><br />
          	Kenny is one of the founders and is currently the president of Bear and Bull Media
Group. Kenny is a second year Financial Analysis Risk Management (FARM) student. He has a
strong interest in the stock market, stock picking strategies and security analysis. In addition, he
is interested in studying derivatives products including options, futures, swaps and forwards. He
has experience in day trading, swing trading, options trading, and value investing. He enjoys
discussion regarding the economy, stock market, interest rate outlook and other market moving
news. He will be writing the weekly report (publish on Saturdays) which consists of a weekly
summary of the global stock market, a preview of the next trading week, an economic event
calendar and an earnings calendar. He will be writing other investing articles relating to technical
and fundamental analysis.<br /><br />Kenny is pursuing the CFA designation. His other interests include playing chess, badminton,
and watching Korean dramas. He is especially fond of biking.<br />
            <br />
            <strong id="AD">Andrew Dhawan: VP Information Technology</strong><br /><br />
            Andrew Dhawan is a second year Descartes Scholar in the Applied Mathematics/Biology Option
program. As the VP IT, Andrew has designed, runs, and routinely updates this website for the
Bear and Bull Media Group.<br /><br />Andrew is also involved in other activities at the University of Waterloo, such as the UW Hindu
Students Associaton, as well as the UW Quiz Bowl Club. Andrew also enjoys reading, solving
math problems and puzzles, and jogging.<br />
            <br />
            <strong id="TC">Tarini Chandak: VP Communications</strong><br />
            <br />
            Tarini Chandak is first year student at the University of Waterloo in the Accounting and
Financial Management Program and a fellow of the School of Accounting and Finance. In her
role as VP communications at Bear and Bull Media Group, Tarini aims to create opportunities
for the organization to effectively communicate internally and externally through the expansion
of current communication channel, and partnerships with organization both within and outside of
the University of Waterloo.<br /><br />On campus, Tarini is also involved with the Accounting and Finance Student Association, The
RightFit Promotions team, and she is currently working on her own entrepreneurial venture. In
her free time, Tarini likes to get involved, blog, watch TV, and have good conversation.<br />
            <br />
            <strong id="MI">Mateen Imran: Editor-in-Chief</strong><br /><br />
            Mateen is a first year student at the University of Waterloo in the Accounting and Financial
Management program. As Editor-in-Chief, Mateen's objectives are to ensure the quality of each
and every article, summary and report published by Bear and Bull. Mateen is also responsible for
the timely publishing of new material on the website, certifying that there is always new content
to enjoy.<br /><br />Mateen is involved in a plethora of other extra-curricular activities on and off campus, such as
serving as a high school ambassador for the School of Accounting and Finance, as well as his
thorough involvement in the Accounting and Finance Student Association. In his spare time,
Mateen enjoys watching basketball and football (go Colts!), as well as playing video games and
chilling with his friends.<br />
            <br />
            <strong id="RB">Rahul Bhambhani: VP Admin & Finance</strong><br /><br />
            Rahul Bhambhani is a first year student at the University of Waterloo in the Accounting and
Financial Management program. As VP of admin and finance at the Bear and Bull Media Group,
Rahul's objective is to ensure all the financial needs of the club are met in order to successfully
run events as well as everyday operations. In addition, Rahul is also responsible for admin duties
that help the club operate efficiently.<br /><br />In addition, Rahul is involved in extra-curricular activities on and off campus, including DECA
U and the Canadian Cancer Society. In his free time, Rahul enjoys playing sports, especially
soccer, playing video games, and socializing with friends.<br />
            <br />
            <strong id="HW">Haylin Wang: VP Marketing</strong><br /><br />
            Haylin is a first year student in the Accounting and Financial Management Program and a Fellow
of the School of Accounting and Finance at the University of Waterloo. Her role, as the VP
Marketing, is to promote Bear and Bull and increase student awareness, participation and market
reach in both readers and analysts.<br /><br />She is currently developing our new website with Andrew, our VP Information Technology,
and Edward Zhang, an Electric and Computer Engineering student. Some of the new projects
Haylin envisions to implement during this term include: a new logo, members' business cards,
guest speaker events, professional training package, and e-newsletters. Members of the Bear
and Bull Marketing Team will have many great opportunities to express their creativity and
professionalism through leading and participating in these projects.<br /><br />Haylin is pursuing a CA designation. She is also an active member of the SAF Ambassador
Program, Living-Learning Community, Toastmasters, and Waterloo Investment Research
Exchange (WIREX). During her leisure time, she is an enthusiastic volunteer, voice actor, singer,
and a thirsty reader.<br />
